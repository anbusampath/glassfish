package org.glassfish.api;

import java.io.File;
import java.lang.annotation.Documented;
import java.lang.annotation.Retention;
import java.lang.annotation.Target;

import static java.lang.annotation.ElementType.FIELD;
import static java.lang.annotation.ElementType.METHOD;
import static java.lang.annotation.ElementType.PARAMETER;
import static java.lang.annotation.RetentionPolicy.SOURCE;

/**
 * Used in conjunction with {@link File} or {@link String} that represents a file to indicate that the path is
 * absolutized.
 *
 * <p>
 * Note that using {@link String} to represent a path name is discouraged, because of the lack of type safety.
 *
 * @author Kohsuke Kawaguchi
 */
@Retention(SOURCE)
@Documented
@Target({ METHOD, PARAMETER, FIELD })
public @interface Absolutized {
}
